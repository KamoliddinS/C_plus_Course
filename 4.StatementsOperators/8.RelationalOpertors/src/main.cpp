#include <iostream>

int main() {
    // Demonstrating Equal to (==)
    int a = 5;
    int b = 5;
    if (a == b) {
        std::cout << "a and b are equal." << std::endl;
    }

    // Demonstrating Not equal to (!=)
    int c = 5;
    int d = 6;
    if (c != d) {
        std::cout << "c and d are not equal." << std::endl;
    }

    // Demonstrating Greater than (>)
    int e = 10;
    int f = 5;
    if (e > f) {
        std::cout << "e is greater than f." << std::endl;
    }

    // Demonstrating Less than (<)
    int g = 5;
    int h = 10;
    if (g < h) {
        std::cout << "g is less than h." << std::endl;
    }

    // Demonstrating Greater than or equal to (>=)
    int i = 10;
    int j = 10;
    if (i >= j) {
        std::cout << "i is greater than or equal to j." << std::endl;
    }

    // Demonstrating Less than or equal to (<=)
    int k = 5;
    int l = 5;
    if (k <= l) {
        std::cout << "k is less than or equal to l." << std::endl;
    }

    return 0;
}
